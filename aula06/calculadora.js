Adição
function soma(a, b) {
    return a + b;
}

//Subtração
function subtracao(c, d) {
    return c - d;
}

//Multiplicação
function multiplicacao(e, f) {
    return e * f;
}

//Divisão
function divisao(g, h) {
    return g / h;
}

console.log(soma(0, 1));
console.log(subtracao(3, 1));
console.log(multiplicacao(1, 3));
console.log(divisao(8, 2));


